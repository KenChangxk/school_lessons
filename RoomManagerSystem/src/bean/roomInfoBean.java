package bean;

public class roomInfoBean {
	private String roomNum;
	private int roomSeats;
	private String roomClass;
	private String roomDept;
	private String roomRequire;
	public String getRoomNum() {
		return roomNum;
	}
	public void setRoomNum(String roomNum) {
		this.roomNum = roomNum;
	}
	public int getRoomSeats() {
		return roomSeats;
	}
	public void setRoomSeats(int roomSeats) {
		this.roomSeats = roomSeats;
	}
	public String getRoomClass() {
		return roomClass;
	}
	public void setRoomClass(String roomClass2) {
		this.roomClass = roomClass2;
	}
	public String getRoomDept() {
		return roomDept;
	}
	public void setRoomDept(String roomDept) {
		this.roomDept = roomDept;
	}
	public String getRoomRequire() {
		return roomRequire;
	}
	public void setRoomRequire(String roomRequire) {
		this.roomRequire = roomRequire;
	}
	
}
